from __future__ import annotations

import json
import os
import re
from datetime import datetime, timezone
from typing import Any, Dict, List, Optional

import psycopg
from psycopg.rows import dict_row

DATABASE_URL = os.getenv("DATABASE_URL", "").strip()


def now_utc_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def _normalize_sql(sql: str) -> str:
    return sql.replace("?", "%s")


class PGCursorWrapper:
    def __init__(self, cursor: Any):
        self._cursor = cursor

    def execute(self, sql: str, params: tuple | list | None = None):
        sql = sql.strip()

        pragma_match = re.match(r"PRAGMA\s+table_info\(([\w_]+)\)", sql, re.IGNORECASE)
        if pragma_match:
            table_name = pragma_match.group(1)
            self._cursor.execute(
                """
                SELECT
                    ordinal_position AS cid,
                    column_name AS name,
                    data_type AS type
                FROM information_schema.columns
                WHERE table_schema = 'public' AND table_name = %s
                ORDER BY ordinal_position
                """,
                (table_name,),
            )
            return self

        self._cursor.execute(_normalize_sql(sql), params or ())
        return self

    def fetchone(self):
        row = self._cursor.fetchone()
        return dict(row) if row is not None else None

    def fetchall(self):
        rows = self._cursor.fetchall()
        return [dict(r) for r in rows]

    @property
    def rowcount(self) -> int:
        return self._cursor.rowcount


class PGConnectionWrapper:
    def __init__(self, conn: Any):
        self._conn = conn
        self.row_factory = None

    def cursor(self) -> PGCursorWrapper:
        return PGCursorWrapper(self._conn.cursor())

    def execute(self, sql: str, params: tuple | list | None = None) -> PGCursorWrapper:
        cur = self.cursor()
        cur.execute(sql, params or ())
        return cur

    def commit(self) -> None:
        self._conn.commit()

    def close(self) -> None:
        self._conn.close()


def get_conn() -> PGConnectionWrapper:
    if not DATABASE_URL:
        raise RuntimeError("DATABASE_URL is required for Postgres mode")
    conn = psycopg.connect(DATABASE_URL, row_factory=dict_row)
    return PGConnectionWrapper(conn)


def row_to_dict(row: Optional[Dict[str, Any]]) -> Optional[Dict[str, Any]]:
    return dict(row) if row else None


def rows_to_dicts(rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    return [dict(r) for r in rows]


def add_column_if_missing(conn: PGConnectionWrapper, table_name: str, column_name: str, ddl: str) -> None:
    rows = conn.execute(f"PRAGMA table_info({table_name})").fetchall()
    cols = [str(r["name"]) for r in rows]
    if column_name not in cols:
        conn.execute(f"ALTER TABLE {table_name} ADD COLUMN {ddl}")
    conn.commit()


def init_db() -> None:
    conn = get_conn()
    cur = conn.cursor()

    cur.execute(
        """
        CREATE TABLE IF NOT EXISTS scans (
            scan_id TEXT PRIMARY KEY,
            status TEXT NOT NULL,
            created_at TEXT NOT NULL,
            user_id INTEGER NOT NULL DEFAULT 1
        )
        """
    )

    cur.execute(
        """
        CREATE TABLE IF NOT EXISTS findings (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            scan_id TEXT NOT NULL,
            service TEXT,
            severity TEXT,
            check_id TEXT,
            title TEXT,
            resource_id TEXT,
            status TEXT,
            created_at TEXT NOT NULL,
            evidence TEXT
        )
        """
    )

    cur.execute(
        """
        CREATE TABLE IF NOT EXISTS fix_guidance (
            check_id TEXT PRIMARY KEY,
            title TEXT NOT NULL,
            summary TEXT NOT NULL,
            console_path TEXT NOT NULL,
            steps_json TEXT NOT NULL,
            cli_json TEXT NOT NULL,
            terraform TEXT NOT NULL DEFAULT '',
            updated_at TEXT NOT NULL
        )
        """
    )

    cur.execute(
        """
        CREATE TABLE IF NOT EXISTS finding_actions (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            scan_id TEXT NOT NULL,
            check_id TEXT NOT NULL,
            resource_id TEXT NOT NULL,
            resolution TEXT NOT NULL,
            note TEXT NOT NULL DEFAULT '',
            created_at TEXT NOT NULL,
            UNIQUE(scan_id, check_id, resource_id)
        )
        """
    )

    cur.execute(
        """
        CREATE TABLE IF NOT EXISTS connected_accounts (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            user_id INTEGER NOT NULL DEFAULT 1,
            customer_name TEXT NOT NULL,
            account_name TEXT NOT NULL DEFAULT '',
            aws_account_id TEXT NOT NULL,
            role_arn TEXT NOT NULL,
            external_id TEXT NOT NULL DEFAULT '',
            region TEXT NOT NULL,
            status TEXT NOT NULL DEFAULT 'PENDING',
            is_active BOOLEAN NOT NULL DEFAULT TRUE,
            created_at TEXT NOT NULL,
            updated_at TEXT NOT NULL
        )
        """
    )

    cur.execute(
        """
        CREATE TABLE IF NOT EXISTS scan_account_links (
            scan_id TEXT PRIMARY KEY,
            account_id INTEGER NOT NULL,
            customer_name TEXT NOT NULL,
            account_name TEXT NOT NULL DEFAULT '',
            aws_account_id TEXT NOT NULL,
            role_arn TEXT NOT NULL,
            region TEXT NOT NULL,
            linked_at TEXT NOT NULL
        )
        """
    )

    conn.commit()

    # migrations for existing tables that predate these columns
    add_column_if_missing(conn, "connected_accounts", "user_id", "user_id INTEGER NOT NULL DEFAULT 1")
    add_column_if_missing(conn, "scans", "user_id", "user_id INTEGER NOT NULL DEFAULT 1")

    conn.close()


def save_scan(scan_id: str, status: str) -> None:
    conn = get_conn()
    conn.execute(
        """
        INSERT INTO scans (scan_id, status, created_at)
        VALUES (?, ?, ?)
        ON CONFLICT (scan_id)
        DO UPDATE SET status = EXCLUDED.status
        """,
        (scan_id, status, now_utc_iso()),
    )
    conn.commit()
    conn.close()


def update_scan_user_id(scan_id: str, user_id: int) -> None:
    conn = get_conn()
    conn.execute(
        "UPDATE scans SET user_id = ? WHERE scan_id = ?",
        (user_id, scan_id),
    )
    conn.commit()
    conn.close()


def save_findings(scan_id: str, findings: List[Dict[str, Any]]) -> None:
    conn = get_conn()
    conn.execute("DELETE FROM findings WHERE scan_id = ?", (scan_id,))

    for f in findings:
        conn.execute(
            """
            INSERT INTO findings (
                scan_id,
                service,
                severity,
                check_id,
                title,
                resource_id,
                status,
                created_at,
                evidence
            )
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (
                scan_id,
                f.get("service", ""),
                f.get("severity", ""),
                f.get("check_id", ""),
                f.get("title", ""),
                f.get("resource_id", ""),
                f.get("status", ""),
                f.get("created_at", now_utc_iso()),
                json.dumps(f.get("evidence", {})),
            ),
        )

    conn.commit()
    conn.close()


def get_scan(scan_id: str) -> Optional[Dict[str, Any]]:
    conn = get_conn()
    row = conn.execute(
        "SELECT scan_id, status, created_at, user_id FROM scans WHERE scan_id = ?",
        (scan_id,),
    ).fetchone()
    conn.close()
    return row_to_dict(row)


def get_findings(scan_id: str) -> List[Dict[str, Any]]:
    conn = get_conn()
    rows = conn.execute(
        """
        SELECT
            id,
            scan_id,
            service,
            severity,
            check_id,
            title,
            resource_id,
            status,
            created_at,
            evidence
        FROM findings
        WHERE scan_id = ?
        ORDER BY id ASC
        """,
        (scan_id,),
    ).fetchall()
    conn.close()

    out: List[Dict[str, Any]] = []
    for r in rows:
        item = dict(r)
        raw_evidence = item.get("evidence")
        try:
            item["evidence"] = json.loads(raw_evidence) if raw_evidence else {}
        except Exception:
            item["evidence"] = {}
        out.append(item)
    return out


def list_scans(limit: int = 50, account_id: Optional[int] = None, user_id: Optional[int] = None) -> List[Dict[str, Any]]:
    conn = get_conn()

    if account_id is not None:
        rows = conn.execute(
            """
            SELECT
                s.scan_id,
                s.status,
                s.created_at,
                l.account_id,
                l.customer_name,
                l.account_name,
                l.aws_account_id,
                l.role_arn,
                l.region,
                l.linked_at
            FROM scans s
            LEFT JOIN scan_account_links l ON l.scan_id = s.scan_id
            WHERE l.account_id = ?
              AND (? IS NULL OR s.user_id = ?)
            ORDER BY s.created_at DESC
            LIMIT ?
            """,
            (account_id, user_id, user_id, limit),
        ).fetchall()
    elif user_id is not None:
        rows = conn.execute(
            """
            SELECT
                s.scan_id,
                s.status,
                s.created_at,
                l.account_id,
                l.customer_name,
                l.account_name,
                l.aws_account_id,
                l.role_arn,
                l.region,
                l.linked_at
            FROM scans s
            LEFT JOIN scan_account_links l ON l.scan_id = s.scan_id
            WHERE s.user_id = ?
            ORDER BY s.created_at DESC
            LIMIT ?
            """,
            (user_id, limit),
        ).fetchall()
    else:
        rows = conn.execute(
            """
            SELECT
                s.scan_id,
                s.status,
                s.created_at,
                l.account_id,
                l.customer_name,
                l.account_name,
                l.aws_account_id,
                l.role_arn,
                l.region,
                l.linked_at
            FROM scans s
            LEFT JOIN scan_account_links l ON l.scan_id = s.scan_id
            ORDER BY s.created_at DESC
            LIMIT ?
            """,
            (limit,),
        ).fetchall()

    conn.close()
    return rows_to_dicts(rows)


def get_fix_guidance(check_id: str) -> Optional[Dict[str, Any]]:
    conn = get_conn()
    row = conn.execute(
        """
        SELECT
            check_id,
            title,
            summary,
            console_path,
            steps_json,
            cli_json,
            terraform,
            updated_at
        FROM fix_guidance
        WHERE check_id = ?
        """,
        (check_id,),
    ).fetchone()
    conn.close()

    if not row:
        return None

    return {
        "check_id": row["check_id"],
        "title": row["title"],
        "summary": row["summary"],
        "consolePath": row["console_path"],
        "steps": json.loads(row["steps_json"]) if row.get("steps_json") else [],
        "cli": json.loads(row["cli_json"]) if row.get("cli_json") else [],
        "terraform": row.get("terraform", "") or "",
        "updated_at": row["updated_at"],
    }


