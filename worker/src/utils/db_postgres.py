def delete_connected_account(account_id: int, user_id: Optional[int] = None) -> bool:
    conn = get_conn()

    if user_id is not None:
        existing = conn.execute(
            "SELECT id FROM connected_accounts WHERE id = ? AND user_id = ?",
            (account_id, user_id),
        ).fetchone()
    else:
        existing = conn.execute(
            "SELECT id FROM connected_accounts WHERE id = ?",
            (account_id,),
        ).fetchone()

    if not existing:
        conn.close()
        return False

    linked = conn.execute(
        "SELECT 1 FROM scan_account_links WHERE account_id = ? LIMIT 1",
        (account_id,),
    ).fetchone()
    if linked:
        conn.close()
        raise ValueError("Cannot delete account because scans already exist for this account")

    conn.execute("DELETE FROM connected_accounts WHERE id = ?", (account_id,))
    conn.commit()
    conn.close()
    return True


